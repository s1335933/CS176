public class ArrayAssignment1 {
    public static void main(String[] args) {
        
        double[] values = {25.3, 100, -10, -1.5, 13, 98.6, 17, 123.145, 125.6, 123.146};

        for (double value : values) {
            System.out.print(value + " ");
        }

        System.out.println();
    }
}
